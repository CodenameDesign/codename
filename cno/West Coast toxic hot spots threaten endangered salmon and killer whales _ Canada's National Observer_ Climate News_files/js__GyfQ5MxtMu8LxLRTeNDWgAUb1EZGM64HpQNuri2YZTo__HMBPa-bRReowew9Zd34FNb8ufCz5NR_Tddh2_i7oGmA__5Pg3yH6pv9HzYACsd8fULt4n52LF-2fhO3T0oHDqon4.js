(function ($) {

  $(function() {
    var wrappers = $('.js-eq-height'),
          eh_sets = []; // (eh_sets = Equal Height Sets)

    wrappers.each(function() {
      eh_sets.push({
        'wrapper': this,
        'items': $('.js-eq-height-item', this)
      });
    });
    var equalHeight = function () {
      eh_sets.forEach(function(element) {
        var topHeight = 0,
            items = element.items;

        // in order to set equal heights, we must let the content flow freely first
        items.css({
          'box-sizing': 'content-box',
          'height': 'auto'
        });

        items.each(function() {
          if ($(this).outerHeight() > topHeight) {
            topHeight = $(this).outerHeight();
          }
        });

        items.css({
          'height': topHeight + 'px'
        });
      });
    };

    $(window).bind("load", equalHeight);
    $(window).bind("resize", equalHeight);
  });

})(jQuery);

