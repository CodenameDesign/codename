/**
 * @file
 * Sticky header.
 */
(function ($, Drupal) {

  'use strict';

  Drupal.behaviors.stickyHeader = {
    attach: function () {

      var stickyHeader;
      var headerWrapper = $('#site-header-wrapper');
      var header = $('#site-header');
      var logo = $('#site-logo');
      var logoPathReg = logo.data('logo-reg');
      var logoPathSm = logo.data('logo-sm');

      stickyHeader = new Waypoint({
        element: window,
        offset: -80,
        handler: function (direction) {
          if (direction === 'down') {
            swapLogoSm();
          }
          else if (direction === 'up') {
            swapLogoReg();
          }
        }
      });

      function handleWayPoint() {
        var $width = (window.innerWidth > 0) ? window.innerWidth : document.documentElement.clientWidth;
        let $narrowHeader = $('.site-header--narrow');
        if ($width > 1200 && !$narrowHeader.length) {
          stickyHeader.enable();
          if (window.scrollY > 80 && logo.attr('src') === logoPathReg) {
            swapLogoSm();
          }
        }
        else {
          stickyHeader.disable();
          if (logo.attr('src') === logoPathSm) {
            swapLogoReg();
          }
        }
      }

      function swapLogoSm() {
        headerWrapper.addClass('site-header-wrapper--sm');
        header.addClass('site-header--sm');
        logo.attr('src', logoPathSm);
      }

      function swapLogoReg() {
        headerWrapper.removeClass('site-header-wrapper--sm');
        header.removeClass('site-header--sm');
        logo.attr('src', logoPathReg);
      }

      handleWayPoint();

      $(window).resize(function () {
        handleWayPoint();
      });
    }
  };

})(jQuery, Drupal);

